import pandas as pd
import shutil
import os
import sys

def moveBad():
    df = pd.read_csv('./BadData/bad.csv')
    df.head()

    for index, row in df.iterrows():
      name = row['fullname']
      src = f'./data/{name}'
      if (os.path.exists(src)):
          dst = f'./BadData/{name}'
          shutil.move(src, dst)


def moveDup():
    df = pd.read_csv('./Duplicates/dups.csv')
    df.head()

    for index, row in df.iterrows():
      name = row['name']
      src = f'./data/{name}.jpg'
      if (os.path.exists(src)):
          dst = f'./Duplicates/{name}.jpg'
          shutil.move(src, dst)


